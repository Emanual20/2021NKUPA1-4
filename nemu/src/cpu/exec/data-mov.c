make_EHelper(cltd) {
  if (decoding.is_operand_size_16) {
    rtl_sext(&t0, &cpu.eax, 2);
    rtl_shri(&cpu.edx, &t0, 16);
  }
  else {
    rtl_sari(&cpu.edx, &cpu.eax, 31);
  }

  print_asm(decoding.is_operand_size_16 ? "cwtl" : "cltd");
}

